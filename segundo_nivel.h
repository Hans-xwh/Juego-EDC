bool checkColi(int x, int y) { //false=NoMover  true=SiMover
	for (int i = 0; i < persona.height; i++) {
		for (int j = 0; j < persona.width; j++) {
			if (LaberintoMatriz[y+i][x+j] == 1) {
				return false;
			}
		}
	}
	return true;
	
}




